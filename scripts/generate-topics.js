"use strict";


const fs = require("fs");
const path = require("path");


const GROQ_API_KEY = process.env.GROQ_API_KEY;


const ROOT = path.join(__dirname, "..");


const PURPOSE_FILE =
    path.join(ROOT, "purposes.js");


const CATEGORY_FILE =
    path.join(ROOT, "categories.js");


const OUTPUT_FILE =
    path.join(ROOT, "generated", "topics.js");



function loadJS(file){

    let content = fs.readFileSync(file,"utf8");

    content =
        content
        .replace(/const .*?=\s*Object\.freeze\(/,"")
        .replace(/\);\s*Object\.freeze\(.*?\);/s,"")
        .replace(/\);\s*$/,"");


    return eval(content);

}



function extractCategories(){


    const categories =
        loadJS(CATEGORY_FILE);


    return categories;

}




async function askGroq(category){


    const prompt = `

You are a global LinkedIn content strategist.

Create professional LinkedIn post topics.

Purpose:
${category.purpose}


Category:
${category.label}


Generate exactly 40 topics.


Rules:

- Global audience
- Professional
- No duplicate ideas
- Suitable for LinkedIn
- No hashtags
- No emojis
- No marketing spam


Return JSON only:

[
 {
  "id":"",
  "label":"",
  "category":"${category.id}",
  "description":""
 }
]

`;



    const response =
        await fetch(
        "https://api.groq.com/openai/v1/chat/completions",
        {

        method:"POST",

        headers:{
            "Authorization":
            `Bearer ${GROQ_API_KEY}`,

            "Content-Type":
            "application/json"
        },


        body:JSON.stringify({

            model:
            "llama-3.3-70b-versatile",

            temperature:
            0.4,

            messages:[

                {
                role:"user",
                content:prompt
                }

            ]

        })

        });



    const data =
        await response.json();



    let text =
        data.choices[0]
        .message.content;


    text =
    text.replace(/```json/g,"")
        .replace(/```/g,"")
        .trim();



    return JSON.parse(text);

}




async function generate(){


    if(!GROQ_API_KEY){

        throw new Error(
        "Missing GROQ_API_KEY"
        );

    }


    const categories =
        extractCategories();



    let topics=[];



    for(const category of categories){


        console.log(
        "Generating:",
        category.label
        );


        const result =
            await askGroq(category);



        topics.push(...result);


    }



    const output =

`"use strict";

/* ==========================================================================
   VIDHWAAN AI Writer
   Generated Topic Library

   Auto Generated By GitHub Actions + Groq AI
   ========================================================================== */


const VW_TOPICS = Object.freeze(
${JSON.stringify(topics,null,4)}
);


Object.freeze(VW_TOPICS);
`;



    fs.mkdirSync(
        path.dirname(OUTPUT_FILE),
        {
            recursive:true
        }
    );


    fs.writeFileSync(
        OUTPUT_FILE,
        output
    );


    console.log(
    "Generated:",
    topics.length,
    "topics"
    );


}




function validate(){


    const file =
    fs.readFileSync(
        OUTPUT_FILE,
        "utf8"
    );


    if(!file.includes("VW_TOPICS")){

        throw new Error(
        "Invalid topics.js"
        );

    }


    console.log(
    "Validation successful"
    );


}



if(
process.argv.includes("--validate")
){

    validate();

}
else{

    generate();

}
