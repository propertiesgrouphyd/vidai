"use strict";

const fs = require("fs");
const path = require("path");


const ROOT = path.join(__dirname, "..");


const PURPOSE_FILE =
    path.join(ROOT, "purposes.js");


const CATEGORY_FILE =
    path.join(ROOT, "categories.js");


const OUTPUT_FILE =
    path.join(ROOT, "generated", "topics.js");


const GROQ_API_KEY =
    process.env.GROQ_API_KEY;



/*
    Extract array from frontend JS files

    Supports:

    const VW_CATEGORIES = Object.freeze([
       ...
    ]);

*/


function loadArrayFromJS(filePath, variableName) {


    const content =
        fs.readFileSync(
            filePath,
            "utf8"
        );


    const declaration =
        `const ${variableName}`;


    const start =
        content.indexOf(
            declaration
        );


    if (start === -1) {

        throw new Error(
            `${variableName} not found`
        );

    }



    const arrayStart =
        content.indexOf(
            "[",
            start
        );


    if (arrayStart === -1) {

        throw new Error(
            `${variableName} array missing`
        );

    }



    let depth = 0;

    let arrayEnd = -1;



    for (
        let i = arrayStart;
        i < content.length;
        i++
    ) {


        if (content[i] === "[") {

            depth++;

        }


        else if (content[i] === "]") {


            depth--;


            if (depth === 0) {

                arrayEnd = i;
                break;

            }

        }

    }



    if (arrayEnd === -1) {

        throw new Error(
            `Unable to read ${variableName}`
        );

    }



    const arrayText =
        content.substring(
            arrayStart,
            arrayEnd + 1
        );



    return eval(arrayText);

}




function getPurposes() {


    return loadArrayFromJS(
        PURPOSE_FILE,
        "VW_PURPOSES"
    );

}




function getCategories() {


    return loadArrayFromJS(
        CATEGORY_FILE,
        "VW_CATEGORIES"
    );

}





async function generateWithGroq(category) {


    const prompt = `

You are a world-class LinkedIn content strategist.

Generate LinkedIn post topics.

Purpose:
${category.purpose}


Category:
${category.label}


Category description:
${category.description}


Generate exactly 30 unique topics.


Rules:

- Global audience
- Professional quality
- Suitable for LinkedIn
- Useful for professionals
- No duplicates
- No hashtags
- No emojis
- Avoid generic topics


Return ONLY JSON.


Format:

[
 {
  "id":"short-kebab-case-id",
  "label":"Topic Name",
  "category":"${category.id}",
  "description":"Short explanation"
 }
]


`;



    const response =
        await fetch(
            "https://api.groq.com/openai/v1/chat/completions",
            {

                method: "POST",


                headers: {

                    "Authorization":
                    `Bearer ${GROQ_API_KEY}`,

                    "Content-Type":
                    "application/json"

                },


                body: JSON.stringify({

                    model:
                    "llama-3.3-70b-versatile",


                    temperature:
                    0.3,


                    messages: [

                        {
                            role:"user",
                            content:prompt
                        }

                    ]

                })

            }
        );



    const data =
        await response.json();



    if (!data.choices) {

        console.log(data);

        throw new Error(
            "Groq API failed"
        );

    }



    let text =
        data.choices[0]
        .message
        .content;



    text =
        text
        .replace(/```json/g, "")
        .replace(/```/g, "")
        .trim();



    return JSON.parse(text);

}





async function generateTopics() {


    if (!GROQ_API_KEY) {

        throw new Error(
            "GROQ_API_KEY missing"
        );

    }



    const purposes =
        getPurposes();



    const categories =
        getCategories();



    console.log(
        "Purposes:",
        purposes.length
    );


    console.log(
        "Categories:",
        categories.length
    );



    let topics = [];




    for (const category of categories) {



        console.log(
            "Generating:",
            category.purpose,
            "→",
            category.label
        );



        const result =
            await generateWithGroq(
                category
            );



        topics.push(
            ...result
        );



    }




    const output =

`"use strict";

/* ==========================================================================
   VIDHWAAN AI Writer

   Generated Topic Library

   Auto Generated By GitHub Actions + Groq AI

   Flow:

   Purpose → Category → Topic
   ========================================================================== */


const VW_TOPICS = Object.freeze(

${JSON.stringify(topics, null, 4)}

);


Object.freeze(VW_TOPICS);
`;




    fs.mkdirSync(

        path.dirname(OUTPUT_FILE),

        {
            recursive:true
        }

    );



    fs.writeFileSync(

        OUTPUT_FILE,

        output

    );



    console.log(

        "Generated Topics:",
        topics.length

    );

}





function validateTopics() {



    if (!fs.existsSync(OUTPUT_FILE)) {


        throw new Error(
            "topics.js missing"
        );


    }



    const content =
        fs.readFileSync(
            OUTPUT_FILE,
            "utf8"
        );



    if (
        !content.includes(
            "VW_TOPICS"
        )
    ) {


        throw new Error(
            "Invalid topics.js"
        );


    }



    console.log(
        "Validation successful"
    );


}






if (
    process.argv.includes("--validate")
) {


    validateTopics();


}

else {


    generateTopics();


}
