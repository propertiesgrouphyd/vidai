"use strict";


const fs = require("fs");
const path = require("path");



const ROOT =
    path.join(__dirname, "..");



const PURPOSE_FILE =
    path.join(ROOT, "purposes.js");


const CATEGORY_FILE =
    path.join(ROOT, "categories.js");


const OUTPUT_FILE =
    path.join(ROOT, "generated", "topics.js");


const PROGRESS_FILE =
    path.join(ROOT, "generated", "topics-progress.json");



const GROQ_API_KEY =
    process.env.GROQ_API_KEY;



const MODEL =
    "llama-3.3-70b-versatile";



function sleep(ms){

    return new Promise(
        r=>setTimeout(r,ms)
    );

}





function loadArrayFromJS(filePath, variableName){


    const content =
        fs.readFileSync(
            filePath,
            "utf8"
        );


    const start =
        content.indexOf(
            `const ${variableName}`
        );


    if(start === -1){

        throw new Error(
            `${variableName} missing`
        );

    }



    const arrayStart =
        content.indexOf(
            "[",
            start
        );



    let depth=0;
    let end=-1;



    for(
        let i=arrayStart;
        i<content.length;
        i++
    ){

        if(content[i]==="[")
            depth++;


        if(content[i]==="]"){

            depth--;

            if(depth===0){

                end=i;
                break;

            }

        }

    }



    return eval(
        content.substring(
            arrayStart,
            end+1
        )
    );

}





function getCategories(){

    return loadArrayFromJS(
        CATEGORY_FILE,
        "VW_CATEGORIES"
    );

}






async function generateWithGroq(batch){



const prompt = `

You are a global LinkedIn content strategist.


Generate LinkedIn topic library.


Categories:

${batch.map(c=>`

Category ID:
${c.id}

Category Name:
${c.label}

Description:
${c.description}

`).join("\n")}



Generate exactly 20 topics per category.


Rules:

- Professional global quality
- Evergreen topics
- Useful and actionable
- No hashtags
- No emojis
- No duplicates
- No generic topics


Return ONLY JSON.


Format:

[
{
"id":"short-kebab-case",
"label":"Topic Name",
"category":"category-id",
"description":"short explanation"
}
]


`;




let delay = 60000;



while(true){



const response =
await fetch(
"https://api.groq.com/openai/v1/chat/completions",
{

method:"POST",

headers:{

"Authorization":
`Bearer ${GROQ_API_KEY}`,

"Content-Type":
"application/json"

},


body:JSON.stringify({

model:MODEL,

temperature:0.2,


messages:[

{
role:"user",
content:prompt
}

]

})


}
);



const data =
await response.json();





if(response.ok && data.choices){


let text =
data.choices[0]
.message
.content
.trim();



text =
text
.replace(/```json/g,"")
.replace(/```/g,"")
.trim();




const start =
text.indexOf("[");


const end =
text.lastIndexOf("]");



if(start!==-1 && end!==-1){

text =
text.substring(
start,
end+1
);

}



let result =
JSON.parse(text);




return result;


}






if(response.status===429){


let retry =
response.headers.get(
"retry-after"
);



let wait =
retry
?
Number(retry)*1000
:
delay;



console.log(
`Groq rate limit. Waiting ${Math.round(wait/1000)} seconds`
);



await sleep(wait);



delay =
Math.min(
delay*2,
900000
);



continue;


}




console.log(data);


throw new Error(
"Groq API failed"
);



}


}







function saveProgress(topics){


fs.mkdirSync(
path.dirname(PROGRESS_FILE),
{
recursive:true
}
);



fs.writeFileSync(

PROGRESS_FILE,

JSON.stringify(
topics,
null,
4
)

);


}








function createOutput(topics){



const output =

`"use strict";

/* ==========================================================================
   VIDHWAAN AI Writer

   Generated Topic Library

   Auto Generated By GitHub Actions + Groq AI

   Flow:

   Purpose → Category → Topic
   ========================================================================== */


const VW_TOPICS = Object.freeze(

${JSON.stringify(topics,null,4)}

);


Object.freeze(VW_TOPICS);


export default VW_TOPICS;
`;



fs.mkdirSync(
path.dirname(OUTPUT_FILE),
{
recursive:true
}
);



fs.writeFileSync(
OUTPUT_FILE,
output
);


}









function validate(topics){



const ids =
new Set();



for(const t of topics){


if(
!t.id ||
!t.label ||
!t.category ||
!t.description
){

throw new Error(
"Invalid topic detected"
);

}



if(ids.has(t.id)){

throw new Error(
"Duplicate topic id: "+t.id
);

}


ids.add(t.id);


}



console.log(
"Validation OK:",
topics.length
);


}










async function generate(){



if(!GROQ_API_KEY){

throw new Error(
"GROQ_API_KEY missing"
);

}



const categories =
getCategories();



console.log(
"Categories:",
categories.length
);



let topics=[];



if(
fs.existsSync(PROGRESS_FILE)
){


topics =
JSON.parse(
fs.readFileSync(
PROGRESS_FILE,
"utf8"
)
);


console.log(
"Resume:",
topics.length
);


}





const completed =
new Set(
topics.map(
t=>t.category
)
);






for(
let i=0;
i<categories.length;
i+=5
){



const batch =
categories
.slice(i,i+5)
.filter(
c=>!completed.has(c.id)
);



if(batch.length===0)
continue;



console.log(
"Generating:",
batch.map(
x=>x.label
)
);




const result =
await generateWithGroq(
batch
);





for(const c of batch){


const items =
result.filter(
x=>x.category===c.id
);



if(items.length!==20){


throw new Error(
`${c.id} generated ${items.length} topics`
);


}


topics.push(
...items
);


}





saveProgress(
topics
);



console.log(
"Saved:",
topics.length
);



await sleep(10000);



}





validate(topics);



createOutput(topics);



console.log(
"Completed:",
topics.length
);



}










if(
process.argv.includes("--validate")
){

if(fs.existsSync(PROGRESS_FILE)){

validate(
JSON.parse(
fs.readFileSync(
PROGRESS_FILE,
"utf8"
)
)
);

}

}
else{


generate()
.catch(
err=>{

console.error(err);

process.exit(1);

}
);


}
